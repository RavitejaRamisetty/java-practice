package com.basic;

public class Unaryarthoperators 
{
	public static void main(String[] args)
	{
		int num;
		num=3;
		System.out.println("num=" + num++);
		System.out.println("num=" + num);
		int num1;
		num1=3;
		System.out.println("num1=" + num1--);
		System.out.println("num1=" +num1);
		int num2;
		num2=3;
		System.out.println("num2=" + ++num2);
		int num3;
		num3=3;
		System.out.println("num3=" + --num3);
	}

}
